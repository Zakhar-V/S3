#include "Time.hpp"

//----------------------------------------------------------------------------//
// Time
//----------------------------------------------------------------------------//

//----------------------------------------------------------------------------//
void Time::Update(void)
{
	double _ct = arctic::easy::Time();
	if (m_prevTime == 0)
		m_prevTime = _ct;
	m_deltaTime = (float)(_ct - m_prevTime);
	m_prevTime = _ct;
}

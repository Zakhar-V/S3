#include "Time.hpp"

//----------------------------------------------------------------------------//
// Time
//----------------------------------------------------------------------------//

//----------------------------------------------------------------------------//
bool Time::OnEvent(int _type, void* _arg)
{
	switch (_type)
	{
	case SystemEvent::BeginFrame:
	{
		double _ct = arctic::easy::Time();
		if (m_prevTime == 0)
			m_prevTime = _ct;
		m_deltaTime = (float)(_ct - m_prevTime);
		m_prevTime = _ct;
	} break;
	}

	return false;
}
